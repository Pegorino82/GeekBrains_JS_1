"use strict";

/**
 * функция складывает два числа
 * @param {number} a
 * @param {number} b
 * @returns {number} сумма a и b
 */
function addTwo(a, b) {
    return a + b
}

/**
 * функция вычитает из числа а число b
 * @param {number} a
 * @param {number} b
 * @returns {number} разность a и b
 */
function subTwo(a, b) {
    return a - b
}

/**
 * функция перемножает два числа
 * @param {number} a
 * @param {number} b
 * @returns {number} произведение a и b
 */
function mulTwo(a, b) {
    return a * b
}

/**
 * функция делит a на b
 * @param {number} a
 * @param {number} b
 * @returns {number} частное a и b
 */
function divTwo(a, b) {
    return a / b
}
