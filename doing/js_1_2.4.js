"use strict";

function addTwo(a, b) {
    /**
     * функция складывает два числа
     */
    return a + b
}

function subTwo(a, b) {
    /**
     * функция вычитает из числа а число b
     */
    return a - b
}

function mulTwo(a, b) {
    /**
     * функция перемножает два числа
     */
    return a * b
}

function divTwo(a, b) {
    /**
     * функция делит a на b
     */
    return a / b
}
